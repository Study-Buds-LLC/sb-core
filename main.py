def weekly_summary(students):
    print("\n--- Weekly Summary ---")
    for student in students:
        print(f"{student.name} participated in {len(student.scores)} quizzes and earned {len(student.badges)} badges!")
    print("----------------------\n")

def leaderboard(students):
    print("\n--- Leaderboard ğŸ† ---")
    sorted_students = sorted(students, key=lambda s: s.average_score(), reverse=True)
    for i, student in enumerate(sorted_students, 1):
        print(f"{i}. {student.name} - Avg Score: {student.average_score():.2f}")
    print("----------------------\n")

